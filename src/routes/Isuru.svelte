<h2>•Man and horse head</h2>
<h3>මිනිසා හා අශ්ව හිස</h3>

<p>  This carving of a human and a horse's head is called Agni (fire) and Parjanaya (cloud). Therefore, Isurumuniya is one of the must visit historical sites. <br/> මිනිසෙක් හා අශ්ව හිසක කැටයමක් වන මෙය අග්නි (ගින්න) හා පාර්ජන්‍ය (වළාකුළු) යනුවෙන් හදුන්වති. එම නිසා ඓතිහාසික ස්ථාන අතරින් ඉසුරුමුණිය නැරඹිය යුතු ම ස්ථානයකි.</p>
