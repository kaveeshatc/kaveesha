<h2>•Isurumuni Pemuwala</h2>
<h3>ඉසුරුමුණි පෙම්යුවළ</h3>

<p>A 6th century mystical sculpture. A woman sits on a man's lap. She raises a finger. It may be that she claims to be a coyote. But the man is careless. The carvings depict King Dutu Gemunu's son Prince Saliya and his girlfriend, a saddle girl named Ashokamala. It is said that Sally abandoned her kingship because of her. <br/> 6 වැනි සියවසෙහි ගුප්ත කලාවට අයිති කැටයමකි. කාන්තාවක් පුරුෂයාගේ උකුලෙහි වාඩි වී සිටී. ඇය ඇඟිල්ලක් ඔසවා සිටින්නීය. ඒ ඇගේ කෝල බව ප්‍රකාශ කරන්නට විය හැක. එහෙත් පුරුෂයා අනවධානයෙන් සිටී. මෙම කැටයමෙහි නිරූපණය වන්නේ දුටු ගැමුණු රජුගේ පුත් සාලිය කුමරු සහ ඔහුගේ පෙම්වතිය වූ අශෝකමාලා නම් සැඩොල් තරුණිය යන දෙදෙනා යයි සිතිය හැකිය. ඇය නිසා තමනට උරුම වන්නට තිබූ රජකම සාලිය විසින් අත්හරින ලදැයි කියැවේ.</p>
