<p> *BC In the third century, King Devanampiya Tissa dedicated this cave to the Maha Sangha. This temple holds a prominent place among the temples dedicated to the Buddha Sasana by him. <br/>  ක්‍රි.පූ. තෙවන සියවසේ දී, දේවානම්පිය තිස්ස රජතුමා විසින් මෙම ලෙන මහා සංඝ රත්නය විෂයෙහි පූජා කර තිබේ. එතුමා විසින් බුදු සසුනට පූජා කරන ලද විහාර අතර ප්‍රධාන ස්ථානයක් මෙම විහාරයට හිමි වේ.</p>
<br/>
<br/>
<p> *The Tripitaka was also revived in Pali under the leadership of several Buddhist scholars during the 10 years (1981-1991) at the Matale Aluvihara. <br/>  ත්‍රිපිටකයද බොදු පඩිවරුන් කිහිපදෙනෙකුගේ මුලිකත්වයෙන් යලිත් පාලි භාෂාවෙන් තල්පත්වල ග්‍රන්තාරුඩ කිරීම මාතලේ අලුවිහාරේදීම සිදුකරන ලදී.</p>
<br/>
<br/>
<p> *Aluvihara is a village in the Matale District of the Central Province. Aluviharaya Rajamaha Viharaya is located in this area. <br/>  අළුවිහාරය යනු මධ්‍යම පළාතේ මාතලේ දිස්ත්‍රික්කයේ පිහිටි ගම්මානයකි. අළුවිහාරය රජමහ විහාරය මෙම ප්‍රදේශයේ පිහිටා ඇත.</p>
