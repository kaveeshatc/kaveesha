<h2>•Geological features </h2>
<h3>භූ විද්‍යාත්මක ලක්‍ෂණ</h3>

<p> Sigiriya is a rock formed by igneous igneous rocks emanating from an eroded volcano. It rises above the plains around the canal and is visible for miles on either side. It is located on a steep slope that rises suddenly above the plain around the rock. The rock is 180 meters high and sloping on all sides. Most places are extruded as a base. There is a steep slope down the oval elongated defensive shape. <br/>සීගිරිය, ඛාදනයට ලක්වූ ගිණිකන්දකින් පිටවී සිසිල් වුණු ආග්නේය පාෂාණ යන්ගෙන් නිර්මාණය වූ පර්වතයකි. ඇල හාත්පස පිහිටි තැනිතලාවෙන් ඉහළට නැගුණු එය ඕනෑම පැත්තකින් සැතපුම් ගණනාවක් ඈතට දිස්වේ. පර්වතය වටා තිබෙන තැන්නෙන් එක්වරම ඉහළට නැගුණු අධික බෑවුමකින් යුක්ත ගොඩනැගිල්ලක් මත එය පිහිටා ඇත. පර්වතය මීටර් 180 ක් උස වන අතර සෑම පැත්තකින්ම ප්‍රපාතාකාරය. බොහෝ ස්ථාන පාදම ලෙසට නෙරා ඇත. අන්ඩාකාර දිගු ආරක්ෂක හැඩයෙන් පහළට ක්‍රමිකව දිවෙන බෑවුමක් ඇත.

  </p>
