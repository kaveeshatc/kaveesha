<h2>•History of Isurumuniya</h2>
<h3>ඉසුරුමුණියේ ඉතිහාසය</h3>

<p>The Isurumuni Temple was built by King Devanampiyatissa who ruled in the ancient city of Anuradhapura in ancient Sri Lanka. It was built to house 500 high caste children. King Kasyapa (473 - 491) renovated this temple and named it "Boupulvan Kasubgiri Radmahavehera". The name was created by combining the names of his two daughters and his name.There is a temple connected to a cave, and above it is a rock. There is a small stupa. It seems that the stupa was built today. At the bottom are carved ivory figures depicting a pond rising on either side of an opening. An image of a horse is also carved on the rock. The carving of the Isurumuni couple carved on a stone slab may have been carved elsewhere and later fixed at that place. There is a wedding garden near the temple.  <br/>  පැරණි ශ්‍රී ලංකාවේ අනුරාධපුර නම් අග නගරයේ රජ කළ දේවානම්පියතිස්ස රජතුමා විසින් ඉසුරුමුණි විහාරය ගොඩ නංවන ලදී. උසස් කුලදරුවන් 500ක් මහණ කොට ඔවුනට වැඩ වාසය කිරීම පිණිස එය තනවන ලදී. කාශ්‍යප රජු විසින් (473 - 491) මෙම විහාරය පිළිසකර කොට "බෝඋපුල්වන් කසුබ්ගිරි රද්මහවෙහෙර" නමින් නම් කළේය. ඔහුගේ දියණියන් දෙදෙනාගේ නම් සහ ඔහුගේ නම් එක් කොට එම නම නිර්මාණය කර ඇත. ගල් ලෙනකට සම්බන්ධ වූ විහාරයක් එහි තිබෙන අතර, උඩින් ඇත්තේ ගල් පර්වතයකි. එහි කුඩා ස්ථූපයක් තනා ඇත. එම ස්ථූපය ගොඩනගන්නට ඇත්තේ වර්තමානයේ බව පෙනෙන්නට තිබේ. වඩාත් පහළින් විවරයක දෙපැත්තෙන්ම පොකුණක සිට මතු වන ආකාරයක් නිරූපණය වන ඇත් රූප නෙළා ඇත. ගල් පර්වතය මත අශ්වයෙකුගේ රූපයක්ද නෙළා ඇත. ගල් පුවරුවක නෙළන ලද ඉසුරුමුණි පෙම් යුවල සහිත කැටයම වෙනත් තැනක නෙළා පසුව එම ස්ථානයේ සවි කරන්නට ඇත. විහාරයට නුදුරෙන් මගුල් උයන දක්නට ඇත.   </p>
