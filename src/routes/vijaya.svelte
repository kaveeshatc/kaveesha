<h2>King Vijaya</h2>
<p>*According to the Mahavansa chronicle, Prince Vijaya was the first Sinhalese king.<br/> මහාවංශ වංශකතාවට අනුව, විජය කුමරු (ක්‍රි.පූ. 543-505) පළමු සිංහල රජු විය.</p>
<br/>
<p>*Thambapanni was the place where King Vijaya set foot in Ceylon. <br/>  විජය රජු ලංකාවට පය තැබූ ස්තානය තම්බපන්නියයි.  </p>
<br/>
<p>*His farther's name is sinhabahu. <br/>  ඔහුගේ පියාගේ නම සිංහබාහු ය.  </p>
<br/>
<p>*His mother's name is sinhasivali. <br/>  ඔහුගේ මවගේ නම සිංහසීවලී.  </p>
<br/>
<p>*His frist wife's name is kuweni. <br/> ඔහුගේ මුල් බිරිඳගේ නම කුවේණි. </p>
<br/>
<p>*They have be two childrens. <br/> ඔවුන්ට දරුවන් දෙදෙනෙකි. </p>
<br/>
<p>*Their names are Deesala and Jeevahattha. <br/> ඔවුන්ගේ නම් දීසල සහ ජීවහත්ත </p>
<br/>
<p>*After a while, Kuveni was chased away and another princess was brought in. <br/> කාලයාගේ ඇවෑමෙන් කුවේණිය එළවා දමා තවත් කුමාරිකාවක් ගෙන්වා ගන්නා ලදී. </p>
<br/>
<p>*But they had no children. <br/> නමුත් ඔවුන්ට දරුවන් සිටියේ නැත. </p>
<br/>
<p>*The expelled Kuveni was killed by men of her tribe. But Deesala and Jeevahaththa started the Vedda Vedda people. <br/> පලවා හැරූ කුවේනියව ඇයගේ ගෝත්‍රයේ මිනිසුන් විසින් මරා දැමීය. එහෙත් දීසාල හා ජීවහත්ත වන වැදී වැදි ජන පරපුර ආරම්භ කරලීය. </p>
<br/>
<p> Concerned in old age that he would die without an heir, he decided to bring his twin brother Sumitta from India to govern his kingdom. Vijaya sent a letter to Sumitta, but died before receiving a reply. His ministers from Upatissagāma then governed the kingdom for a year while they waited for a reply. In Sinhapura, Sumitta had become king and had three sons. His queen was a daughter of the king of Madda (possibly Madra). When Vijaya's messengers arrived, Sumitta asked one of his sons to go to Lanka because he was too old; Panduvasdeva, his youngest son, volunteered. Panduvasdeva and 32 sons of Sumitta's ministers reached Lanka, where he became the new ruler. <br/> තමාට උරුමක්කාරයෙකු නොමැතිව මිය යනු ඇතැයි මහලු වියේදී කනස්සල්ලට පත් වූ ඔහු තම රාජධානිය පාලනය කිරීමට තම නිවුන් සොහොයුරා වන සුමිත්ත ඉන්දියාවෙන් ගෙන්වා ගැනීමට තීරණය කළේය. විජය සුමිත්තට ලිපියක් යැවූ නමුත් පිළිතුරක් ලැබීමට පෙර මිය ගියේය. උපතිස්සගම සිට ඔහුගේ ඇමතිවරු පිළිතුරක් බලාපොරොත්තුවෙන් සිටියදී වසරක් රාජ්‍යය පාලනය කළහ. සිංහපුරයේදී සුමිත්ත රජ වී පුතුන් තිදෙනෙකු සිටියේය. ඔහුගේ බිසව මද්දා රජුගේ දියණියකි. විජයගේ දූතයන් පැමිණි විට සුමිත්ත තම පුතෙකුට වයස වැඩි නිසා ලංකාවට යන ලෙස ඉල්ලා සිටියේය. ඔහුගේ බාල පුත් පඬුවස්දේව ස්වේච්ඡාවෙන් ඉදිරිපත් විය. පඬුවස්දේව සහ සුමිත්තගේ අමාත්‍යවරුන්ගේ පුතුන් 32 දෙනා ලංකාවට පැමිණි අතර එහිදී ඔහු නව පාලකයා බවට පත් විය.
</p>
